function generateLossHistory(steps) {
  const history = []
  let trainLoss = 1.2
  let evalLoss = 1.28
  for (let i = 1; i <= steps; i += 5) {
    trainLoss = Math.max(0.65, trainLoss - (Math.random() * 0.012) + (Math.random() * 0.003))
    evalLoss = Math.max(0.72, evalLoss - (Math.random() * 0.010) + (Math.random() * 0.004))
    history.push({ step: i, train: +trainLoss.toFixed(4), eval: +evalLoss.toFixed(4) })
  }
  return history
}

